package Learn;

public class LoopBintang{
    public static void main(String[] args){

        System.out.println(" ==== START ==== ");

        int a = 1;

        for(a=1; a <= 4; a++){
            System.out.println(" * " + a);
        }

        int b = 4;
        for(b=4; b >= 1; b--){
            System.out.println(" * " + b);
        }
        
        System.out.println(" ==== FINISH ==== ");

    }
}